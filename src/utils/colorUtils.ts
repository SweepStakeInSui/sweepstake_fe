type Colors = {
  [key: string]: string;
};
function hexToRgb(hex: string): string {
  const cleanedHex = hex.replace(/^#/, '');
  const bigint = parseInt(cleanedHex, 16);
  const r = (bigint >> 16) & 255;
  const g = (bigint >> 8) & 255;
  const b = bigint & 255;
  return `${r}, ${g}, ${b}`;
}

function generateColorWithOpacity(color: string, name: string): Colors {
  const colors: Colors = {};
  for (let i = 100; i >= 0; i -= 5) {
    const opacity = (i / 100).toFixed(2);
    colors[`${name}-a${i}`] = `rgba(${hexToRgb(color)}, ${opacity})`;
  }
  return colors;
}
